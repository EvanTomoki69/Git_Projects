#Para crear un entorno virtual, podemos usar python -m venv <nombre_ambiente> 
#Esto generara un ambiente virtual para poder levantar nuestro proyecto en Python
#Para verificar esto, hacemos lo siguiente en el cmd C:...>"nombre_ambiente"\Scripts\activate 
#Para instalar una libreria simplemente es pip install <libreria>
#Para instalar ipython vamos a escribir pip install ipython, esta es una libreria interactiva que posee funciones de autocompletado
#Para salir escribimos exit
#Para finalizar nuestro entorno virtual usaremos C:...>deactivate